#include <stdio.h>  // Include the standard input-output library to enable using printf for console output

int main()  // Main function, the entry point of the program
{
    printf("H");  // Print the letter "H"
    printf("e");  // Print the letter "e"
    printf("l");  // Print the letter "l"
    printf("l");  // Print the second letter "l"
    printf("o");  // Print the letter "o"
    printf(" ");  // Print a space character
    printf("W");  // Print the letter "W"
    printf("o");  // Print the letter "o"
    printf("r");  // Print the letter "r"
    printf("l");  // Print the letter "l"
    printf("d");  // Print the letter "d"
    printf("!");  // Print the exclamation mark "!"
    
    return 0;  // Return 0 to the operating system, indicating successful execution
}
